package Ex_01_JAVA_BASIC;

public class Lab20_Literals {
    public static void main(String[] args) {
        int age = 65;
        float PI = 3.14F;
        boolean is_Married = true;
        int Enum = 9;
        int binary_num = 0b1010; // Binary number system literal prefixed by 0b
        int oct_num = 0101;
        int hex_num = 0xface;
        System.out.println(age);
        System.out.println(is_Married);
        System.out.println(Enum);
        System.out.println((PI));
        System.out.println(binary_num);
        System.out.println(oct_num);
        System.out.println(hex_num);
    }
}